class AccessibleEntriesQTests(VaultServiceMixin, TestCase):

    def test_user_can_access_own_entries(self):
        self._make_entry(self.alice)
        q = VaultService.accessible_entries_q(self.alice)
        self.assertEqual(LoginEntry.objects.filter(q).count(), 1)

    def test_user_cannot_access_other_users_entries(self):
        self._make_entry(self.bob)
        q = VaultService.accessible_entries_q(self.alice)
        self.assertEqual(LoginEntry.objects.filter(q).count(), 0)

    def test_isolation_is_symmetric(self):
        self._make_entry(self.alice)
        self._make_entry(self.bob)
        self.assertEqual(LoginEntry.objects.filter(VaultService.accessible_entries_q(self.alice)).count(), 1)
        self.assertEqual(LoginEntry.objects.filter(VaultService.accessible_entries_q(self.bob)).count(), 1)

