class VerifyUnlockTests(VaultServiceMixin, TestCase):

    def test_returns_true_and_key_on_correct_hash(self):
        vault = self._setup_vault(self.alice, client_hash='correct-hash')
        ok, key = VaultService.verify_unlock(vault, 'correct-hash')
        self.assertTrue(ok)
        self.assertEqual(key, 'encrypted-vault-key')

    def test_returns_false_on_wrong_hash(self):
        vault = self._setup_vault(self.alice, client_hash='correct-hash')
        ok, key = VaultService.verify_unlock(vault, 'wrong-hash')
        self.assertFalse(ok)
        self.assertEqual(key, '')

    def test_returns_false_when_vault_not_setup(self):
        vault = VaultService.create_vault(self.alice)
        ok, key = VaultService.verify_unlock(vault, 'any-hash')
        self.assertFalse(ok)
        self.assertEqual(key, '')

    def test_empty_hash_does_not_unlock(self):
        vault = self._setup_vault(self.alice, client_hash='real-hash')
        ok, key = VaultService.verify_unlock(vault, '')
        self.assertFalse(ok)
        self.assertEqual(key, '')

    def test_unlock_is_per_vault(self):
        v1 = self._setup_vault(self.alice, name='Personal', client_hash='hash-1')
        v2 = self._setup_vault(self.alice, name='Work', client_hash='hash-2')
        ok1, _ = VaultService.verify_unlock(v1, 'hash-1')
        ok2, _ = VaultService.verify_unlock(v2, 'hash-1')  # wrong hash for v2
        self.assertTrue(ok1)
        self.assertFalse(ok2)


