class VaultServiceMixin:
    """Create two independent users for each test."""

    def setUp(self):
        self.alice = User.objects.create_user(username='alice_vs', email='alice@test.com', password='pass')
        self.bob = User.objects.create_user(username='bob_vs', email='bob@test.com', password='pass')

    def _setup_vault(self, user, name='Personal', client_hash='hash-abc') -> Vault:
        """Create and fully set up a vault. kdf_salt is server-generated by create_vault."""
        vault = VaultService.create_vault(user, name=name)
        return VaultService.setup_vault(
            vault=vault,
            client_master_hash=client_hash,
            protected_vault_key='encrypted-vault-key',
        )

    def _make_entry(self, user, vault=None, deleted=False) -> LoginEntry:
        if vault is None:
            vault = VaultService.create_vault(user)
        entry = LoginEntry.objects.create(
            vault=vault,
            encrypted_name='enc-name',
            type=PasswordEntry.EntryType.LOGIN,
        )
        if deleted:
            entry.deleted_at = timezone.now()
            entry.save(update_fields=['deleted_at'])
        return entry

    def _shared_vault(self, owner, member, role=VaultMember.Role.VIEWER) -> Vault:
        """Create a vault owned by *owner* and give *member* accepted access."""
        vault = VaultService.create_vault(owner, name='Shared')
        VaultMember.objects.create(
            vault=vault,
            user=member,
            role=role,
            status=VaultMember.Status.ACCEPTED,
            protected_vault_key='enc-key-for-member',
        )
        return vault


