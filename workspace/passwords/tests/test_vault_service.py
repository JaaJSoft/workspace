class DeleteVaultTests(VaultServiceMixin, TestCase):

    def test_owner_can_delete_vault(self):
        vault = VaultService.create_vault(self.alice)
        vault_pk = vault.pk
        result = VaultService.delete_vault(self.alice, vault_pk)
        self.assertTrue(result)
        self.assertFalse(Vault.objects.filter(pk=vault_pk).exists())

    def test_non_owner_cannot_delete_vault(self):
        vault = VaultService.create_vault(self.alice)
        vault_pk = vault.pk
        result = VaultService.delete_vault(self.bob, vault_pk)
        self.assertFalse(result)
        self.assertTrue(Vault.objects.filter(pk=vault_pk).exists())

    def test_manager_member_cannot_delete_vault(self):
        vault = self._shared_vault(self.alice, self.bob, role=VaultMember.Role.MANAGER)
        vault_pk = vault.pk
        result = VaultService.delete_vault(self.bob, vault_pk)
        self.assertFalse(result)
        self.assertTrue(Vault.objects.filter(pk=vault_pk).exists())

    def test_delete_cascades_entries(self):
        vault = VaultService.create_vault(self.alice)
        entry = self._make_entry(self.alice, vault=vault)
        entry_pk = entry.pk
        result = VaultService.delete_vault(self.alice, vault.pk)
        self.assertTrue(result)
        self.assertEqual(LoginEntry.objects.filter(pk=entry_pk).count(), 0)

    def test_delete_cascades_members(self):
        vault = self._shared_vault(self.alice, self.bob)
        member = VaultMember.objects.filter(vault=vault).first()
        member_pk = member.pk
        result = VaultService.delete_vault(self.alice, vault.pk)
        self.assertTrue(result)
        self.assertEqual(VaultMember.objects.filter(pk=member_pk).count(), 0)

    def test_delete_nonexistent_vault_returns_false(self):
        result = VaultService.delete_vault(self.alice, uuid.uuid4())
        self.assertFalse(result)


