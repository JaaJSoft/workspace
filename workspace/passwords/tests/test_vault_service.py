class GenerateKdfSaltTests(VaultServiceMixin, TestCase):

    def test_returns_nonempty_string(self):
        salt = VaultService.generate_kdf_salt(self.alice)
        self.assertTrue(salt)
        self.assertIsInstance(salt, str)

    def test_salt_fits_in_field(self):
        salt = VaultService.generate_kdf_salt(self.alice)
        self.assertLessEqual(len(salt), 44)

    def test_salts_are_unique_per_call(self):
        s1 = VaultService.generate_kdf_salt(self.alice)
        s2 = VaultService.generate_kdf_salt(self.alice)
        self.assertNotEqual(s1, s2)

    def test_salts_differ_across_users(self):
        # Salts for different users differ (with overwhelming probability)
        sa = VaultService.generate_kdf_salt(self.alice)
        sb = VaultService.generate_kdf_salt(self.bob)
        self.assertNotEqual(sa, sb)

    def test_salt_is_base64url_compatible(self):
        import base64
        salt = VaultService.generate_kdf_salt(self.alice)
        # Should decode without error (add padding if needed)
        padded = salt + '=' * (-len(salt) % 4)
        decoded = base64.urlsafe_b64decode(padded)
        self.assertEqual(len(decoded), 32)


