class SetupVaultTests(VaultServiceMixin, TestCase):

    def test_marks_vault_as_setup(self):
        vault = self._setup_vault(self.alice)
        self.assertTrue(vault.is_setup)

    def test_stores_protected_vault_key(self):
        vault = self._setup_vault(self.alice)
        self.assertEqual(vault.protected_vault_key, 'encrypted-vault-key')

    def test_preserves_server_generated_kdf_salt(self):
        vault = VaultService.create_vault(self.alice)
        original_salt = vault.kdf_salt
        VaultService.setup_vault(vault=vault, client_master_hash='h', protected_vault_key='k')
        vault.refresh_from_db()
        self.assertEqual(vault.kdf_salt, original_salt)

    def test_rotation_can_replace_kdf_salt(self):
        vault = self._setup_vault(self.alice)
        new_salt = 'B' * 43
        VaultService.setup_vault(
            vault=vault, client_master_hash='new', protected_vault_key='new-key',
            kdf_salt=new_salt,
        )
        vault.refresh_from_db()
        self.assertEqual(vault.kdf_salt, new_salt)

    def test_master_password_hash_is_not_stored_raw(self):
        vault = self._setup_vault(self.alice, client_hash='my-secret-hash')
        self.assertNotEqual(vault.master_password_hash, 'my-secret-hash')
        self.assertTrue(vault.master_password_hash)

    def test_optional_kdf_algorithm_is_updated(self):
        vault = VaultService.create_vault(self.alice)
        VaultService.setup_vault(
            vault=vault,
            client_master_hash='h',
            protected_vault_key='k',
            kdf_algorithm=Vault.KdfAlgorithm.ARGON2ID,
            kdf_memory=65536,
            kdf_parallelism=4,
        )
        vault.refresh_from_db()
        self.assertEqual(vault.kdf_algorithm, Vault.KdfAlgorithm.ARGON2ID)
        self.assertEqual(vault.kdf_memory, 65536)
        self.assertEqual(vault.kdf_parallelism, 4)

    def test_optional_kdf_iterations_is_updated(self):
        vault = VaultService.create_vault(self.alice)
        VaultService.setup_vault(
            vault=vault, client_master_hash='h', protected_vault_key='k',
            kdf_iterations=800_000,
        )
        vault.refresh_from_db()
        self.assertEqual(vault.kdf_iterations, 800_000)

    def test_setup_is_persisted_to_db(self):
        vault = self._setup_vault(self.alice)
        from_db = Vault.objects.get(pk=vault.pk)
        self.assertTrue(from_db.is_setup)
        self.assertTrue(from_db.protected_vault_key)

    def test_rotation_replaces_existing_protected_key(self):
        vault = self._setup_vault(self.alice, client_hash='old-hash')
        VaultService.setup_vault(
            vault=vault,
            client_master_hash='new-hash',
            protected_vault_key='new-encrypted-key',
        )
        vault.refresh_from_db()
        self.assertEqual(vault.protected_vault_key, 'new-encrypted-key')

    def test_each_vault_has_independent_setup(self):
        v1 = self._setup_vault(self.alice, name='Personal', client_hash='hash-1')
        v2 = VaultService.create_vault(self.alice, name='Work')
        self.assertTrue(v1.is_setup)
        self.assertFalse(v2.is_setup)


